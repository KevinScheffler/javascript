// Number Methods
const num = 999;

// toString
console.log(num.toString() + 111);

// toFixed
console.log('$' + num.toFixed(2));

// Math.min, Math.max, parseInt
console.log(Math.min(12, 222, 891231, 5));
console.log(Math.max(12, 222, 891231, 5));

// Math.floor, Math.ceil, parseInt
console.log(Math.ceil(999.001));
console.log(Math.floor(999.999));
console.log(parseInt(999.999));

